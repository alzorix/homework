'''(№ 3098) У исполнителя Калькулятор две команды, которым присвоены номера:

1. прибавь 1
2. умножь на 1,5

Первая из них увеличивает на 1 число на экране, вторая увеличивает это число
в 1,5 раза, если число чётное. К нечётным числам вторая команда неприменима. Сколько есть программ, которые число 1 преобразуют в число 20? '''

def F(current,end):

    if current == end:
        return 1
    elif current > end:
        return 0
    else:
        if current% 2 ==0:
            return F(current*1.5,end) + F(current+1,end)
        else:
            return F(current + 1, end)



print(F(1,20))