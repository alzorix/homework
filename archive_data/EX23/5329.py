''''(№ 5329) (ЕГЭ-2022) Исполнитель преобразует число, записанное на экране. У исполнителя есть две команды, которым присвоены номера:

1. Вычти 1
2. Найди целую часть от деления на 2

Первая команда уменьшает число на экране на 1,
 вторая заменяет число на экране на целую часть от деления числа на 2.
 Сколько существует программ,
  для которых при исходном числе 30 результатом является число 1,
  и при этом траектория вычислений содержит число 12? '''

def F(start,end):
    if start == end:
        return 1
    elif start < end:
        return 0
    else:
        return  F(start-1,end)+ F(start//2,end)

print(F(30,12)*F(12,1))
