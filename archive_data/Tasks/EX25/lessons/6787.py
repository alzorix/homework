'''(№ 6787) (ЕГЭ-2023) Назовём маской числа последовательность цифр, в которой также могут встречаться следующие символы:
— символ «?» означает ровно одну произвольную цифру;
— символ «*» означает любую последовательность цифр произвольной длины; в том числе «*» может задавать и пустую последовательность.
Например, маске 123*4?5 соответствуют числа 123405 и 12300425.
Найдите все числа, меньшие 10^8, соответствующие маске 3?1*57 и делящиеся без остатка на 2023. В качестве ответа приведите
все найденные числа в порядке возрастания, справа от каждого числа выведите результат его деления на 2023. '''
from fnmatch import *

for i in range(2023,10**8,2023):
    if fnmatch(str(i),"3?1*57"):
        print(i,i//2023)


