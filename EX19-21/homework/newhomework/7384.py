'''(№ 7384) (А. Минак) Два игрока, Петя и Ваня, играют в следующую игру. Перед игроками лежит куча камней.
 Игроки ходят по очереди, первый ход делает Петя. За один ход игрок может добавить в кучу один камень либо увеличить количество камней в куче в два раза.
 Например, имея кучу из 10 камней, за один ход можно получить кучу из 11 или 20 камней. Для того чтобы делать ходы, у игроков есть только 80 камней, включая те,
  которые находятся в куче в начальный момент. Игра завершается в тот момент, когда количество камней в куче становится не менее 61. Победителем считается игрок,
   сделавший последний ход. В начальный момент в куче было S камней; 1 ≤ S ≤ 60. Будем говорить, что игрок имеет выигрышную стратегию,
    если он может выиграть при любых ходах противника.
Ответьте на следующие вопросы:
  Вопрос 1.Укажите количество значений S, при которых Петя может выиграть своим первым ходом.
  Вопрос 2. Найдите два наименьших значения S, когда Петя имеет выигрышную стратегию, причём одновременно выполняются два условия:
– Петя не может выиграть за один ход;
– Петя может выиграть своим вторым ходом независимо от того, как будет ходить Ваня.
Найденные значения запишите в ответе в порядке возрастания.
  Вопрос 3. Найдите значение S, при котором одновременно выполняются два условия:
– у Вани есть выигрышная стратегия, позволяющая ему выиграть первым или вторым ходом при любой игре Пети;
– у Вани нет стратегии, которая позволит ему гарантированно выиграть первым ходом. '''